        this.paciente.setFechaNacimiento(fecha.getText());
        this.paciente.setGrupoSanguineo(gruposanuineo.getSelectedItem().toString());
        if (positivo.isSelected()) {
            this.paciente.setRh("+");
        } else {
            this.paciente.setRh("-");
        }
        
        this.paciente.setNumeroDonaciones((int) donaciones.getValue());

        // En este punto también se podrían guardar los cambios en un 
        // fichero o en una BD
        // Cierro el dialogo
        this.dispose();
