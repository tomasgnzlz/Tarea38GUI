    // Este método privado permite cargar los datos en los componentes
    // de este jdialog del registro seleccionado en el jtable de la ventana
    private void mostrarDatosEditar() {
        // Obtengo el id de la persona seleccionada
        // Para ello, obtengo la fila seleccionada y luego el id de esa fila
        int fila = filaSeleccionadaJTable(padre.getJTable());
        // El id de la persona es el valor de la columna cero de esa fila
        String idPersona = String.valueOf(padre.getJTable().getValueAt(fila, 0));
        // Guarda la persona seleccionada
        this.paciente = padre.getListaPacientes().getPaciente(idPersona); // Muestra datos de la persona que se seleccionó en el jtable
        // en los jtextfield
        nombre.setText(this.paciente.getNombre());
        fecha.setText(this.paciente.getFechaNacimiento());

        // "O", "A", "B", "AB"
//        switch (this.paciente.getGrupoSanguineo().to) {
//            case "O" ->
//                gruposanuineo.setSelectedIndex(0);
//            case "A" ->
//                gruposanuineo.setSelectedIndex(1);
//            case "B" ->
//                gruposanuineo.setSelectedIndex(2);
//            case "AB" ->
//                gruposanuineo.setSelectedIndex(3);
//        }
        this.paciente.setGrupoSanguineo(gruposanuineo.getSelectedItem().toString());
        
        if (this.paciente.getRh().equals("-")) {
            negativo.setSelected(true);
            positivo.setSelected(false);
        } else {
            positivo.setSelected(true);
            negativo.setSelected(false);
        }
        donaciones.setValue(this.paciente.getNumeroDonaciones());
    }

    // Este método permite obtener la fila seleccionada en el JTable
    private int filaSeleccionadaJTable(JTable jTable1) {
        int fila = jTable1.getSelectedRow();
        return fila;
    }

